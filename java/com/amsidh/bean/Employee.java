package com.amsidh.bean;

public class Employee {
	private Integer empId;
	private String empName;
	private String empCompany;
	private Integer yearOfExperiance;
	private String address;
	private String emailId;

	public Integer getEmpId() {
		return empId;
	}

	public void setEmpId(Integer empId) {
		this.empId = empId;
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public String getEmpCompany() {
		return empCompany;
	}

	public void setEmpCompany(String empCompany) {
		this.empCompany = empCompany;
	}

	public Integer getYearOfExperiance() {
		return yearOfExperiance;
	}

	public void setYearOfExperiance(Integer yearOfExperiance) {
		this.yearOfExperiance = yearOfExperiance;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

}
